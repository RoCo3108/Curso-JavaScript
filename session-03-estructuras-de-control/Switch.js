// Setencias Switch 

let nota = 5;

switch (nota) {
    case 5:
        console.log("Aprobaste");
        break;

    case 4:
        console.log("Muy bien Aprobaste,puedes mejorarlo");
        break;
    case 3:
        console.log("bien aprobaste, paso raspando");
        break;
    case 2:
        console.log("Reprobaste");
        break;
    case 1:
        console.log("No estudio nada perdiste");
        break;
    default:
        console.log("Introduzcas una nota del 1 al 5");
        break;
}