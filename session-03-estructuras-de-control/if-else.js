// If else  + if else

let nota = 0;

if (nota === 5) {

    console.log("Muy Bien felicitaciones, has Aprobado");
} else if (nota === 4) {
    console.log("Buen trabajo, podrias haberlo hecho mejor");
} else if (nota === 3) {
    console.log("Has obtenido un suficiente");
} else if (nota === 2) {
    console.log("No has aprobado por poco");
} else if (nota === 1) {
    console.log("No has estudiado nada, trabaja un poco mas para la proxima reprobastes");
} else {
    console.log("Error Introduzcas una nota entre 1 al 5");
}